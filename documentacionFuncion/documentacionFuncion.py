def perimetro_cuadrado(lado):
    """Cualcular el perimetro de un cuadrado
    Esta funcion recibe el valor de un lado de un cuadrado
     y a partir de esta calcula y retorna su perimetro

     Arg:
        lado (int):medida del lado del cuadrado

    Returns:
        perimetro (int):perimtreo del cuadrado

    """

    perimetro=lado*4
    return perimetro


perimetro_cuadrado(lado=5)
